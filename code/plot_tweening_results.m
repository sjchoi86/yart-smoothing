function plot_tweening_results(...
    secs_concat_1khz,q_revs_concat_1khz,...
    tick_fr_fr,tick_fr_to,...
    tick_tween_fr,tick_tween_to,...
    tick_to_fr,tick_to_to,...
    varargin ...
    )
%
% Plot tweening results
%

% Parse options
iP = inputParser;
addParameter(iP,'fig_idx_offset',0);
addParameter(iP,'fig_pos_yoffset',0);
parse(iP,varargin{:});
fig_idx_offset  = iP.Results.fig_idx_offset;
fig_pos_yoffset = iP.Results.fig_pos_yoffset;

% Compute
L_concat_1khz = size(q_revs_concat_1khz,1);

% Compute velocity, acceleration, and jerk of the original concatenated trajectories
dt = (secs_concat_1khz(end)-secs_concat_1khz(1))/length(secs_concat_1khz);
[A_vel,A_acc,A_jerk] = get_A_vel_acc_jerk(L_concat_1khz,dt);
vel  = A_vel*q_revs_concat_1khz;
acc  = A_acc*q_revs_concat_1khz;
jerk = A_jerk*q_revs_concat_1khz;

% Plot
axes_info = [0.12,0.12,0.83,0.8];
fig_idx = 1+fig_idx_offset; USE_DRAGZOOM = 1; afs = 12; lw = 1; tfs = 12;
set_fig(figure(fig_idx),'pos',[0.0,0.65-fig_pos_yoffset,0.25,0.25],...
    'AXIS_EQUAL',0,'USE_DRAGZOOM',USE_DRAGZOOM,...
    'axes_info',axes_info,'ax_str','t [sec]','ay_str','x(t)','afs',afs);
plot(secs_concat_1khz(tick_fr_fr:tick_fr_to,:),...
    q_revs_concat_1khz(tick_fr_fr:tick_fr_to,:),...
    'LineStyle','-','Color','r','LineWidth',lw);
plot(secs_concat_1khz(tick_tween_fr:tick_tween_to,:),...
    q_revs_concat_1khz(tick_tween_fr:tick_tween_to,:),...
    'LineStyle','-','Color','k','LineWidth',lw);
plot(secs_concat_1khz(tick_to_fr:tick_to_to,:),...
    q_revs_concat_1khz(tick_to_fr:tick_to_to,:),...
    'LineStyle','-','Color','b','LineWidth',lw);
xlim([secs_concat_1khz(1),secs_concat_1khz(end)]);
plot_title('Concatenated Trajectories','fig_idx',fig_idx,'tfs',tfs);

% Plot velocities
fig_idx = 2+fig_idx_offset;
set_fig(figure(fig_idx),'pos',[0.25,0.65-fig_pos_yoffset,0.25,0.25],...
    'AXIS_EQUAL',0,'USE_DRAGZOOM',USE_DRAGZOOM,...
    'axes_info',axes_info,'ax_str','t [sec]','ay_str','x`(t)','afs',afs);
plot(secs_concat_1khz(tick_fr_fr:tick_fr_to,:),...
    vel(tick_fr_fr:tick_fr_to,:),...
    'LineStyle','-','Color','r','LineWidth',lw);
plot(secs_concat_1khz(tick_tween_fr:tick_tween_to,:),...
    vel(tick_tween_fr:tick_tween_to,:),...
    'LineStyle','-','Color','k','LineWidth',lw);
plot(secs_concat_1khz(tick_to_fr:tick_to_to,:),...
    vel(tick_to_fr:tick_to_to,:),...
    'LineStyle','-','Color','b','LineWidth',lw);
xlim([secs_concat_1khz(1),secs_concat_1khz(end)]);
plot_title('Concatenated Velocities','fig_idx',fig_idx,'tfs',tfs);

% Plot accelerations
fig_idx = 3+fig_idx_offset;
set_fig(figure(fig_idx),'pos',[0.5,0.65-fig_pos_yoffset,0.25,0.25],...
    'AXIS_EQUAL',0,'USE_DRAGZOOM',USE_DRAGZOOM,...
    'axes_info',axes_info,'ax_str','t [sec]','ay_str','x``(t)','afs',afs);
plot(secs_concat_1khz(tick_fr_fr:tick_fr_to,:),...
    acc(tick_fr_fr:tick_fr_to,:),...
    'LineStyle','-','Color','r','LineWidth',lw);
plot(secs_concat_1khz(tick_tween_fr:tick_tween_to,:),...
    acc(tick_tween_fr:tick_tween_to,:),...
    'LineStyle','-','Color','k','LineWidth',lw);
plot(secs_concat_1khz(tick_to_fr:tick_to_to,:),...
    acc(tick_to_fr:tick_to_to,:),...
    'LineStyle','-','Color','b','LineWidth',lw);
xlim([secs_concat_1khz(1),secs_concat_1khz(end)]);
plot_title('Concatenated Accelerations','fig_idx',fig_idx,'tfs',tfs);

% Plot jerk
fig_idx = 4+fig_idx_offset;
set_fig(figure(fig_idx),'pos',[0.75,0.65-fig_pos_yoffset,0.25,0.25],...
    'AXIS_EQUAL',0,'USE_DRAGZOOM',USE_DRAGZOOM,...
    'axes_info',axes_info,'ax_str','t [sec]','ay_str','x```(t)','afs',afs);
plot(secs_concat_1khz(tick_fr_fr:tick_fr_to,:),...
    jerk(tick_fr_fr:tick_fr_to,:),...
    'LineStyle','-','Color','r','LineWidth',lw);
plot(secs_concat_1khz(tick_tween_fr:tick_tween_to,:),...
    jerk(tick_tween_fr:tick_tween_to,:),...
    'LineStyle','-','Color','k','LineWidth',lw);
plot(secs_concat_1khz(tick_to_fr:tick_to_to,:),...
    jerk(tick_to_fr:tick_to_to,:),...
    'LineStyle','-','Color','b','LineWidth',lw);
xlim([secs_concat_1khz(1),secs_concat_1khz(end)]);
plot_title('Concatenated Jerks','fig_idx',fig_idx,'tfs',tfs);

drawnow;
